function applyLanguage(lang) {
  document.documentElement.lang = lang === "th" ? "th" : "en";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const entry = translations[el.getAttribute("data-i18n")];
    if (!entry) return;
    const text = entry[lang] || entry.en;
    if (el.hasAttribute("data-i18n-html")) {
      el.innerHTML = text;
    } else {
      el.textContent = text;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const entry = translations[el.getAttribute("data-i18n-placeholder")];
    if (!entry) return;
    el.setAttribute("placeholder", entry[lang] || entry.en);
  });

  document.querySelectorAll("[data-i18n-content]").forEach((el) => {
    const entry = translations[el.getAttribute("data-i18n-content")];
    if (!entry) return;
    el.setAttribute("content", entry[lang] || entry.en);
  });

  document.querySelectorAll(".lang-toggle-btn").forEach((btn) => {
    btn.textContent = lang === "en" ? "ไทย" : "English";
    btn.setAttribute("aria-label", lang === "en" ? "Switch to Thai" : "Switch to English");
  });

  localStorage.setItem("tc-lang", lang);
}

function toggleLanguage() {
  const current = localStorage.getItem("tc-lang") || "en";
  applyLanguage(current === "en" ? "th" : "en");
}

document.addEventListener("DOMContentLoaded", () => {
  applyLanguage(localStorage.getItem("tc-lang") || "en");
});
